    private boolean checkValues(String aadharnum, String name) {

        if (aadharnum.trim().isEmpty() || name.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter correct details");
            jTextField1.setText("");
            jTextField2.setText("");
            return false;
        }
        Pattern p = Pattern.compile("[0-9]+");
        Matcher m = p.matcher(aadharnum);
        boolean isMatched = m.matches();
        if (isMatched) {
            return true;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Please Enter correct details");
            jTextField1.setText("");
            jTextField2.setText("");
        }
        return false;
    }
