    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //  Customer c = new Customer(jTextField1.getText(),jTextField2.getText());
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/banking", "root", "root");
            String name = jTextField1.getText().trim();
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Enter Correct Name");
                return;
            }
            if (jTextField2.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "enter aadhar details");
                return;
            }
            String aadharnum = jTextField2.getText().trim();

            boolean isValuesCorrect = checkValues("" + aadharnum, name.trim());
            if (isValuesCorrect) {
                conn.setAutoCommit(false);
                String query = "INSERT INTO customertable " + "VALUES(?,?,?)";
                PreparedStatement p = conn.prepareStatement(query);
                p.setLong(1, Long.parseLong(aadharnum));
                p.setString(2, name);
                p.setFloat(3, (float) 0.0);
                p.executeUpdate();
                conn.commit();
                conn.close();
                JOptionPane.showMessageDialog(rootPane, "Customer added successfully");
                jTextField1.setText("");
                jTextField2.setText("");
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
            if (e.getErrorCode() == 1062) { //1062 means primary key already exist in db
                JOptionPane.showMessageDialog(rootPane, "Aadhar Already registered");
                jTextField1.setText("");
                jTextField2.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

